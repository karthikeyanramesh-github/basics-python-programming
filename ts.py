print("<---Ordinary Swapping Method--->")
print("Before Swapping")
i=99
j=88
print("i = ",i)
print("j = ",j)
t=i
i=j
j=t
print("After Swapping")
print("i = ",i)
print("j = ",j)
print("<---Tuples Swapping Method--->")
print("Before Swapping")
a=10
b=20
print("a = ",a)
print("b = ",b)
b,a=(a,b)
print("After Swapping")
print("a = ",a)
print("b = ",b)